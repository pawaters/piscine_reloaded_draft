#ifndef FT_ABS_H
# define FT_ABS_H

#define ABS(Value) ft_abs(Value)

int ft_abs(int nb)
{
	if (nb = -2147483648)
		return (0);
	if (nb < 0)
		nb = -nb;
}
#endif

